public class Demo1 {
    public static void main(String[] args) {
        int i=1;  //变量  记录次数
        while(i<=5){
            //System.out.println(i);
            System.out.println("第"+i+"次输出，隔壁老王");
            //累计次数
            i++;  //等价于i=i+1
        }
    }
}
